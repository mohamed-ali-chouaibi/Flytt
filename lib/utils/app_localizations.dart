      'choose_protection': 'Wähle deinen Schutz',
    },
  };
  Locale _locale = const Locale('en');
  Locale get locale => _locale;
  static Future<AppLocalizations> load(Locale locale) async {
    final appLocalizations = AppLocalizations();
    await appLocalizations._loadLanguage(locale);
    return appLocalizations;
  }
  Future<void> _loadLanguage(Locale locale) async {
    final prefs = await SharedPreferences.getInstance();
    final savedLanguage = prefs.getString(languageCodeKey);
    if (savedLanguage != null) {
      _locale = Locale(savedLanguage);
    } else {
      _locale = locale;
      await prefs.setString(languageCodeKey, locale.languageCode);
    }
  }
  Future<void> setLocale(Locale newLocale) async {
    if (!_localizedValues.containsKey(newLocale.languageCode)) return;
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(languageCodeKey, newLocale.languageCode);
    _locale = newLocale;
  }
  String translate(String key) {
    return _localizedValues[_locale.languageCode]?[key] ?? key;
  }
}
class AppLocalizationsDelegate extends LocalizationsDelegate<AppLocalizations> {
  const AppLocalizationsDelegate();
  @override
  bool isSupported(Locale locale) {
    return ['en', 'fr', 'ar', 'de', 'et', 'lt'].contains(locale.languageCode);
  }
  @override
  Future<AppLocalizations> load(Locale locale) {
    return AppLocalizations.load(locale);
  }
  @override
  bool shouldReload(AppLocalizationsDelegate old) => false;
} 
